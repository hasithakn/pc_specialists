    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        if (!checkBeforeAdd()) {
            if (!supplierNameText.getText().isEmpty()) {
                supIdGen();
                Supplier supplier = new Supplier(supplierIdText.getText(), supplierNameText.getText());
                try {
                    int a = SupplierController.addSupplier(supplier);
                    if (a > 0) {
                        JOptionPane.showMessageDialog(ViewSupplier.this, "Supplier Added");

                        if (nso != null) {
                            nso.fillSupplierCombo();
                            nso.fillSupplierDetailsByName();
                            nso.supplierCombo.getEditor().setItem(supplier.getSupName().toString());
                            nso.supIdText.setText(supplier.getSupId());
                        }

                        fillSupplierCombo();
                        fillSuppliersTable();
                    } else {
                        JOptionPane.showMessageDialog(ViewSupplier.this, "Supplier Adding Failed");
                    }
                } catch (ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(ViewSupplier.this, "Supplier Name Field Is Empty");
            }
        } else {
            JOptionPane.showMessageDialog(ViewSupplier.this, "Supplier Name " + supplierNameText.getText() + " Already Added");
        }


    }//GEN-LAST:event_addButtonActionPerformed

