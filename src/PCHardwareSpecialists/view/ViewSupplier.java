    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        try {
            String supId = supplierTable.getValueAt(supplierTable.getSelectedRow(), 0).toString();
            try {
                int a = SupplierController.deleteSupplier(supId);
                if (a > 0) {
                    JOptionPane.showMessageDialog(ViewSupplier.this, "Supplier Deleted");
                    if (nso != null) {
                        nso.fillSupplierCombo();
                        nso.fillSupplierDetailsByName();
                        nso.supIdText.setText("");
                    }
                    fillSuppliersTable();
                    fillSupplierCombo();
                    supplierIdText.setText("");
                    supplierNameText.setText("");
                } else {
                    JOptionPane.showMessageDialog(ViewSupplier.this, "Supplier Not Deleted");
                }
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(ViewSupplier.this, "Please Select Supplier Before Delete");
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        if (!checkBeforeAdd()) {
            if (!supplierNameText.getText().isEmpty()) {
                Supplier supplier = new Supplier(supplierTable.getValueAt(supplierTable.getSelectedRow(), 0).toString(), supplierNameText.getText());
                try {
                    int a = SupplierController.updateSupplier(supplier);
                    if (a > 0) {
                        JOptionPane.showMessageDialog(ViewSupplier.this, "Supplier Updated");
                        if (nso != null) {
                            nso.fillSupplierCombo();
                            nso.fillSupplierDetailsByName();
                            nso.supIdText.setText("");
                        } else {
                        }

                        fillSupplierCombo();
                        fillSuppliersTable();
                    } else {
                        JOptionPane.showMessageDialog(ViewSupplier.this, "Supplier Updating Failed");
                    }
                } catch (ClassNotFoundException ex) {
                    JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
                }
            } else {
                JOptionPane.showMessageDialog(ViewSupplier.this, "Supplier Name Field Is Empty");
            }
        } else {
            JOptionPane.showMessageDialog(ViewSupplier.this, "Supplier Name " + supplierNameText.getText() + " Already Updated");
        }
    }//GEN-LAST:event_updateButtonActionPerformed

