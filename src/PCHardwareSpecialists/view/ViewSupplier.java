    private void supplierTableMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_supplierTableMouseReleased
        String supId = supplierTable.getValueAt(supplierTable.getSelectedRow(), 0).toString();
        Supplier supplier = null;
        try {
            supplier = SupplierController.searchSupplier(supId);
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
        }
        if (supplier != null) {
            supplierIdText.setText(supplier.getSupId());
            supplierNameText.setText(supplier.getSupName());
        } else {
            supplierIdText.setText("");
            supplierNameText.setText("");
        }
    }//GEN-LAST:event_supplierTableMouseReleased

