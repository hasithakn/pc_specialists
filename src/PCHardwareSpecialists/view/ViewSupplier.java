    public boolean checkBeforeAdd() {
        ArrayList<Supplier> supplierList = null;
        try {
            supplierList = SupplierController.searchSupplierByName(supplierNameText.getText());
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
        }
        if (supplierList.size() > 0) {
            return true;
        } else {
            return false;
        }
    }

    public void fillSuppliersTable() {
        ArrayList<Supplier> supplierList = null;
        try {
            supplierList = SupplierController.getAllSupplier();

        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
        }
        DefaultTableModel dtm = (DefaultTableModel) supplierTable.getModel();
        dtm.setRowCount(0);
        for (Supplier supplier : supplierList) {
            Object[] rowData = {supplier.getSupId(), supplier.getSupName()};
            dtm.addRow(rowData);
        }

    }

    public void fillSupplierCombo() {
        ArrayList<Supplier> supplier = null;
        try {
            supplier = SupplierController.getAllSupplier();
        } catch (ClassNotFoundException ex) {
            JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(ViewSupplier.this, ex.getMessage());
        }
        supplierCombo.removeAllItems();
        for (Supplier supplier1 : supplier) {
            supplierCombo.addItem(supplier1.getSupName());
        }
    }

